/*
Solicitar a entrada de 2 números inteiros
a saída deverá mostrar o resultado de
todos os operadores relacionais
*/
var num1,num2,res;
num1=prompt("Digite um número");
num1=parseInt(num1);
num2=prompt("Digite outro número");
num2=parseInt(num2);
res=(num1==num2);
document.write("Igualdade: "+res+"<br>");